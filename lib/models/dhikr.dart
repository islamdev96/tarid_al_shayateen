  static String getCategoryNameAr(DhikrCategory category) {
    switch (category) {
      case DhikrCategory.morning:
        return 'أذكار الصباح';
      case DhikrCategory.evening:
        return 'أذكار المساء';
      case DhikrCategory.sleep:
        return 'أذكار النوم';
    }
  }

