  static String getCategoryNameAr(DhikrCategory category) {
    switch (category) {
      case DhikrCategory.morning:
        return 'أذكار الصباح';
      case DhikrCategory.evening:
        return 'أذكار المساء';
      case DhikrCategory.sleep:
        return 'أذكار النوم';
      case DhikrCategory.afterPrayer:
        return 'أذكار بعد الصلاة';
      case DhikrCategory.mosque:
        return 'أذكار المسجد';
      case DhikrCategory.wakingUp:
        return 'أذكار الاستيقاظ';
      case DhikrCategory.travel:
        return 'أدعية وأذكار السفر';
    }
  }

  static List<Dhikr> getByCategory(DhikrCategory category) {
    return defaultAzkar.where((d) => d.category == category).toList();
  }

